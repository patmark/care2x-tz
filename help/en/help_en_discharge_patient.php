<h2><img src="<?php echo $root_path; ?>/gui/img/common/default/frage.gif" alt="" width=15 height=15> How to discharge a patient?</h2>
<p>
    <span class="bold" >Step 1:</span> 
    Click "Discharge" in the left registration submenu.
    <br>
    <img src="<?php echo $root_path; ?>/help/en/img/en_menu_discharge.png" alt="" id="screenshot" width=158 height=92>
    <br>
    <span class="bold" >Step 2:</span> 
    Select a patient from the list and click the corresponding 
    <img src="<?php echo $root_path; ?>gui/img/common/default/bestell.gif" alt="" width=16 height=16>
    icon. 
    <br>
    <span class="bold" >Step 3:</span> 
    Make sure the correct discharge date and time are entered.
    <br>
    <span class="bold" >Step 4:</span> 
    Select the discharge type by clicking its corresponding radio button
    <br>
    <span class="bold" >Step 5:</span> 
    Type your name in the "Nurse:" field if it is empty. 
    <br/>
    <img src="<?php echo $root_path; ?>/help/en/img/en_textbox_nurse.png" alt="" id="screenshot" width=299 height=20>
    <br>
    <span class="bold" >Step 6:</span> 
    Check the "Yes, I'm sure. Discharge the patient." field. 
    <br>
    <img src="<?php echo $root_path; ?>/help/en/img/en_check_discharge.png" alt="" width=211 height=19>
    <br>
    <span class="bold" >Step 7:</span> 
    Click the "Discharge" button to discharge the patient or the button "Close or "cancel" if you decide not to discharge the patient.
</p>
